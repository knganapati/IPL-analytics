/**
 * Curated IPL franchise dataset — current as of IPL 2026 season.
 *
 * Why a curated dataset?
 * Career-level franchise aggregates (titles, all-time W/L, season-by-season
 * form, head-to-head, home/away splits, current squads) are not exposed by
 * any single free public API. We aggregate them once, ship them as code, and
 * keep them reviewable & versioned. Numbers reflect publicly reported IPL
 * data through the 2025 season and the 2026 season currently in progress.
 */

export interface PlayerStat {
  name: string;
  role: string;
  matches: number;
  runs?: number | null;
  average?: number | null;
  strikeRate?: number | null;
  wickets?: number | null;
  economy?: number | null;
  bestFigures?: string | null;
}

export interface SeasonWins {
  season: number;
  played: number;
  won: number;
  lost: number;
}

export interface HeadToHead {
  opponentId: string;
  wins: number;
  losses: number;
}

export interface KeyPlayer {
  name: string;
  role: string;
  note: string;
}

export interface Strengths {
  batting: number;
  bowling: number;
  powerplay: number;
  deathOvers: number;
  fielding: number;
}

export interface Team {
  id: string;
  name: string;
  shortName: string;
  primaryColor: string;
  secondaryColor: string;
  homeCity: string;
  founded: number;
  titles: number;
  matches: number;
  wins: number;
  losses: number;
  noResult: number;
  highestScore: string;
  lowestScore: string;
  topBatsman: PlayerStat;
  topBowler: PlayerStat;
  seasonWins: SeasonWins[];
  captain: string;
  coach: string;
  homeGround: string;
  homeWinPct: number;
  awayWinPct: number;
  avgFirstInningsScore: number;
  avgPowerplayScore: number;
  avgDeathOversRunRate: number;
  headToHead: HeadToHead[];
  keyPlayers2026: KeyPlayer[];
  strengths: Strengths;
  funFacts: string[];
  decisionInsights: string[];
}

/* Helper to build h2h list — just opponents + numbers */
const h2h = (entries: Array<[string, number, number]>): HeadToHead[] =>
  entries.map(([opponentId, wins, losses]) => ({ opponentId, wins, losses }));

