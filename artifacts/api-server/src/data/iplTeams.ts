/**
 * Curated IPL franchise data set.
 *
 * Why a curated dataset and not the live CricketData API?
 * The free CricketData.org API focuses on live/upcoming matches and per-match
 * scorecards — it does not directly expose franchise-level career aggregates
 * (per-team total wins across 17 seasons, top batsman/bowler ever, season-wise
 * wins, etc.). To compute those we'd have to walk every IPL match across all
 * seasons via paginated calls and aggregate them ourselves, which would
 * exhaust the free 100 hits/day quota in seconds and still wouldn't return
 * career-long career numbers. So we ship a curated, accurate dataset for the
 * insight payload and use the CricketData API for the live-matches widget
 * (see routes/live.ts).
 *
 * Numbers are based on publicly available IPL season summaries through the
 * 2024 season. They are intended for an "insight at a glance" experience,
 * not record-book precision.
 */

export interface PlayerStat {
  name: string;
  role: string;
  matches: number;
  runs?: number | null;
  average?: number | null;
  strikeRate?: number | null;
  wickets?: number | null;
  economy?: number | null;
  bestFigures?: string | null;
}

